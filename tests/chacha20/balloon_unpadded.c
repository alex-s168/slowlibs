
#include "slowlibs/chacha20.h"

static char const data[] = "SeriousPassword";

static uint8_t const protocol_constant[] = {0x01, 0x02, 0x03, 0x04, 0x05, 0x06,
                                            0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c,
                                            0x0d, 0x0e, 0x0f, 0xfa};

static uint8_t const expected[] = {
    0x47, 0xa9, 0xc9, 0x44, 0xb1, 0x33, 0x0c, 0x79, 0xdb, 0xd5, 0x8b,
    0xfc, 0xa6, 0xd7, 0x46, 0x95, 0x8d, 0xd3, 0x0d, 0xa3, 0x09, 0x99,
    0x3b, 0x31, 0x4f, 0x0d, 0x9d, 0x28, 0x18, 0xc9, 0x4b, 0x00,
};

static uint8_t const salt[] = {
    0xda, 0x0e, 0xb9, 0xe9, 0x8b, 0x48, 0x2a, 0x18,
    0x2f, 0xe3, 0xdf, 0xd3, 0x74, 0x39, 0xa9, 0xdd,
};

int main(int argc, char** argv)
{
  uint8_t hash[32];
  int i;

  (void)argc;
  (void)argv;

  // NOTE: These parameters are too low for actual passwords!
  slowcrypt_balloon_kchacha(hash, protocol_constant, (void*)data,
                            sizeof(data) - 1, salt, 16, 4 * 1024 * 1024, 1, 8,
                            1);

  for (i = 0; i < 32; i++)
    if (hash[i] != expected[i])
      return 1;

  return 0;
}
